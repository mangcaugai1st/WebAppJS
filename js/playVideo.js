function ShowVideoClownFish() {
    var show = document.getElementsByClassName('clownFishVideo')[0];
    show.style.display = "block";
    document.getElementById('backClownFish').style.display = 'block';
    document.getElementById('videoCardClownFish').play();
}

function closeVideoClownFish() {
    var show = document.getElementsByClassName('clownFishVideo')[0];
    show.style.display = "none";
    document.getElementById('backClownFish').style.display = 'none';
    document.getElementById('videoCardClownFish').pause();
}

